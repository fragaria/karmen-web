/**
 * Find item by `sku` property in given item list
 */
export function getItemBySku(items, sku) {
  return items.find(variant => variant.sku === sku)
}

/**
 * Select shipping variant for given country record id.
 */
export function selectShippingVariant(countryId) {
  const country = COUNTRIES.find(country => country.id === countryId)

  if (country) {
    return getItemBySku(SHIPPING_VARIANTS, country.shippingSku || "shipping_outside_eu")
  }

  return undefined
}
